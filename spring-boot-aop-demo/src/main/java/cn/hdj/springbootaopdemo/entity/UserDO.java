package cn.hdj.springbootaopdemo.entity;

/**
 * @author hdj
 * @Description: 用户类
 * @date 8/30/19
 */
public class UserDO {

    private Long id;
    private String name;
    private Integer age;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }
}
